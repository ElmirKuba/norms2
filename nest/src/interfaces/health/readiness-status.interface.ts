/**
 * Контракт ответа readiness-эндпоинта: сервис не только жив, но и готов
 * обслуживать (зависимости доступны). Сейчас проверяется только БД.
 */
export interface ReadinessStatus {
  /** Общая готовность: 'ok' — все зависимости на связи. */
  status: 'ok';
  /** Доступность БД. */
  db: 'up';
  /** Метка времени формирования ответа (ISO 8601, UTC). */
  timestamp: string;
}
